	public void guardarPersona() {
		PersonaDTO persona = new PersonaDTO();
		persona.setNombrePersona(nombrePersona);
		persona.setApellidoPersona(apellidoPersona);
		persona.setFechaNacimiento(fechaNacimiento);
		persona.setLugarNacimiento(lugarNacimiento);
		persona.setDireccion(direccion);
		persona.setTelefono(telefono);
		persona.setEstadoCivil(estadoCivil);
		persona.setCorreo(correo);
		persona.setPerfilProfesional(perfilProfesional);
		persona.setFechaInicial(fechaInicial);
		persona.setFechaFinal(fechaFinal);
		persona.setUniversidad(universidad);
		persona.setTitulo(titulo);
		persona.setNivelEstudio(nivelEstudio);
		persona.setTipoReferencia(tipoReferencia);
		persona.setNombreReferencia(nombreReferencia);
		persona.setRelacionReferencia(relacionReferencia);
		persona.setTelefonoReferencia(telefonoReferencia);
		persona.setDireccionReferencia(direccionReferencia);
		persona.setSalario(salario);
		persona.setHorario(horario);
		persona.setFechaInicialExl(fechaInicialExl);
		persona.setFechaFinalExl(fechaFinalExl);
		persona.setCargoExl(cargoExl);
		persona.setEmpresaExl(empresaExl);
		persona.setFuncionesExl(funcionesExl);
		
		persona.setNombreUsuario(nombreUsuario);
		persona.setContrasena(contrasena);
		
		try {
			gestionFachada.guardarPersona(persona);
		} catch (Exception e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
		}
	}

